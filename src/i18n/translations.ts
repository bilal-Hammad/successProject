export function resolveHabitTitle(
  habit: { title: string; translationKey?: string },
  t: (key: string) => string
): string {
  return habit.translationKey ? t(habit.translationKey) : habit.title;
}

export function translate(
  lang: Language,
  key: string,
  params?: Record<string, string | number>
): string {
  const value = translations[lang][key] ?? key;
  if (!params) return value;
  return value.replace(/\{(\w+)\}/g, (_, k) => String(params[k] ?? `{${k}}`));
}
